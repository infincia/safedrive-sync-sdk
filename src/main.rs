static SSH_PASSWORD: &'static str = "safedrive";
static BLOCK_PATH: &'static str = "/storage";


pub fn add_block_sftp(ssh_session: &Session,
                     chunk_name: &str,
                     chunk_data: &[u8]) -> bool {

    let mut to_write = Vec::new();

    for _ in 0..100 {
        to_write.extend(chunk_data);
    }

    let mut chunk_path = Path::new(BLOCK_PATH).to_owned();
    chunk_path.push(chunk_name);
    println!("sending chunk size {} to: {}", to_write.len(), chunk_path.display());

    let sftp = ssh_session.sftp().unwrap();


    let mut file = match sftp.create(&chunk_path) {
        Ok(file) => file,
        Err(e) => panic!("sftp file create failed: {}", e),
    };

    println!("sftp writing");
    match file.write_all(&to_write) {
        Err(e) => println!("sftp write failed: {}", e),
        _ => println!("sftp wrote: {}", to_write.len())
    };
    true
}


pub fn add_block_scp(ssh_session: &Session,
                 chunk_name: &str,
                 chunk_data: &[u8]) -> bool {

    let mut to_write = Vec::new();

    for _ in 0..100 {
        to_write.extend(chunk_data);
    }

    let mut chunk_path = Path::new(BLOCK_PATH).to_owned();
    chunk_path.push(chunk_name);
    println!("sending chunk size {} to: {}", to_write.len(), chunk_path.display());


    let mut channel = match ssh_session.scp_send(&chunk_path, 0o644, to_write.len() as u64, None) {
        Ok(t) => t,
        Err(e) => panic!("scp send failed: {}", e),
    };

    println!("scp writing");
    let written = channel.write(&to_write).unwrap();
    println!("scp wrote: {}", written);

    true
}

